#include <iostream>
using namespace std;

/*
  使用cout 向控制台输出唐朝诗人王之涣的《登鹳雀楼》（可改为其他古诗）
  登鹳雀楼
  王之涣
  白日依山尽,黄河入海流。
  欲穷千里目，更上一层楼。
 */
int main(int argc, char* argv[])
{
	cout << "     登鹳雀楼" << endl;
	cout << "           --王之涣" << endl;
	cout << "白日依山尽, 黄河入海流。" << endl;
	cout << "欲穷千里目, 更上一层楼。" << endl;
	return 0;
}